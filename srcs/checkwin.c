#include "game.h"

int				checkwin(int **tab)
{
	int i;
	int j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (tab[i][j] == WIN_VALUE)
				return (1);
			j++;
		}
		i++;
	}
	return (0);
}
